/******************************************************************************
      函数说明：显示单个字符
      入口数据：x,y显示坐标
                num 要显示的字符
                fc 字的颜色
                bc 字的背景色
                sizey 字号
                mode:  0非叠加模式  1叠加模式
      返回值：  无
******************************************************************************/
void LCD_Show_Char(u16 x,u16 y,char num,u16 fc,u16 bc,char sizey,char mode)
{
	char temp,sizex,t,m=0;
	u16 i,TypefaceNum;//一个字符所占字节大小
	u16 x0=x;
	sizex=sizey/2;
	TypefaceNum=(sizex/8+((sizex%8)?1:0))*sizey;
	num=num-' ';    //得到偏移后的值
	LCD_Address_Set(x,y,x+sizex-1,y+sizey-1);  //设置光标位置 
	for(i=0;i<TypefaceNum;i++)
	{
		if(sizey==16)		temp=ascii_1608[num][i];		 //调用8x16字体
		else if(sizey==24)	temp=ascii_2412[num][i];		 //调用12x24字体
		else if(sizey==32)	temp=ascii_3216[num][i];		 //调用16x32字体
		else return;
		for(t=0;t<8;t++)
		{
			if(!mode)//非叠加模式
			{
				if(temp&(0x01<<t))LCD_WR_DATA(fc);
				else LCD_WR_DATA(bc);
				m++;
				if(m%sizex==0)
				{
					m=0;
					break;
				}
			}
			else//叠加模式
			{
				if(temp&(0x01<<t))LCD_Draw_Point(x,y,fc);//画一个点
				x++;
				if((x-x0)==sizex)
				{
					x=x0;
					y++;
					break;
				}
			}
		}
	}   	 	  
}


/******************************************************************************
      函数说明：显示汉字串
      入口数据：x,y显示坐标
                *s 要显示的汉字串
                fc 字的颜色
                bc 字的背景色
                sizey 字号 可选 24 32
                mode:  0非叠加模式  1叠加模式
      返回值：  无
******************************************************************************/
void LCD_Show_Chinese(u16 x,u16 y,char *s,u16 fc,u16 bc,char sizey,char mode)
{
	while(*s!=0)
	{
		if(sizey==24) LCD_Show_Chinese24x24(x,y,s,fc,bc,sizey,mode);
		else if(sizey==32) LCD_Show_Chinese32x32(x,y,s,fc,bc,sizey,mode);
		else return;
		s+=2;
		x+=sizey;
	}
}

/******************************************************************************
      函数说明：显示字符串
      入口数据：x,y显示坐标
                *p 要显示的字符串
                fc 字的颜色
                bc 字的背景色
                sizey 字号
                mode:  0非叠加模式  1叠加模式
      返回值：  无
******************************************************************************/
void LCD_Show_String(u16 x,u16 y,const char *p,u16 fc,u16 bc,char sizey)
{   
	if(sizey == 16 || sizey == 24 || sizey == 32)
	{
		x *= (sizey/2);
		y *= (sizey);
		if(x > LCD_W)	x = LCD_W;
		if(y > LCD_H)	y = LCD_W;
	}
	else return;
	
	while(*p!='\0')
	{       
		LCD_Show_Char(x,y,*p,fc,bc,sizey,0);
		x+=sizey/2;
		p++;
	}  
}

/******************************************************************************
      函数说明：显示图片
      入口数据：x,y起点坐标
                length 图片长度
                width  图片宽度
                pic[]  图片数组    
      返回值：  无
******************************************************************************/
void LCD_Show_Picture(u16 x,u16 y,u16 length,u16 width,const unsigned char pic[])
{
	u16 i,j;
	u32 k=0;
	LCD_Address_Set(x,y,x+length-1,y+width-1);
	for(i=0;i<length;i++)
	{
		for(j=0;j<width;j++)
		{
			LCD_WR_DATA8(pic[k*2]);
			LCD_WR_DATA8(pic[k*2+1]);
			k++;
		}
	}
}

